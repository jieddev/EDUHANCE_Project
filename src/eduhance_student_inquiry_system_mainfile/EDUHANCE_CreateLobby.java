    private void createlobbyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createlobbyButtonActionPerformed
        EDUHANCE_InstructorLobby lobby = new EDUHANCE_InstructorLobby();
        
        String lobbyCode = generateLobbyCode();
        lobby.getLobbyCodeField().setText(lobbyCode);
        
        String instructor = instructorField.getText();
        String subject = subjectField.getText();
        String section = sectionField.getText();
        String date = dateField.getText();
        
        lobby.getInstructornameboxField1().setText(instructor);
        lobby.getSubjectboxField1().setText(subject);
        lobby.getSectionboxField1().setText(section);
        lobby.getDateboxField3().setText(date);
        
    
        lobby.setLocationRelativeTo(null);
        lobby.setVisible(true);
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/eduhance_db", "root", "");

            String sql = "INSERT INTO lobby (lobbyCode, lobbyInstructor, lobbySubject, lobbySection, lobbyDate, lobbyStatus) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pstmt = conn.prepareStatement(sql);

            pstmt.setString(1, lobbyCode);
            pstmt.setString(2, instructor);
            pstmt.setString(3, subject);
            pstmt.setString(4, section);
            pstmt.setString(5, date);
            pstmt.setInt(6, 1);

            pstmt.executeUpdate();

            conn.close();

            lobby.setLocationRelativeTo(null);
            lobby.setVisible(true);
            EDUHANCE_StudentLandingPage.instance.dispose();
            this.dispose();

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_createlobbyButtonActionPerformed

