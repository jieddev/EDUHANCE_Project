    private void setInstructorName(){
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/eduhance_db", "root", "");

            String sql = "SELECT firstName, lastName from users where id = ?"; 
            PreparedStatement stmt = conn.prepareStatement(sql);

            
            String rawUserId = UserSession.getInstance().getUserId();
            String cleanUserId = rawUserId.replaceAll("[^0-9]", "");

            stmt.setString(1, cleanUserId);

            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                String firstName = rs.getString("firstName");
                String lastName = rs.getString("lastName");
                instructorField.setText(firstName + " " + lastName);
            }

            rs.close();
            stmt.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
