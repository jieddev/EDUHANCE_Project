    private void savechangesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savechangesButtonActionPerformed
        try {
            String userIdString = UserSession.getInstance().getUserId();
            String idNumber = userIdString.split(",")[0].split(":")[1];
            int userId = Integer.parseInt(idNumber);

            String sql = "UPDATE users SET firstName = ?, middleName = ?, lastName = ?, course = ?, yearLevel = ? WHERE id = ?";
            PreparedStatement pstmt = connection.prepareStatement(sql);
            pstmt.setString(1, firstnameField.getText());
            pstmt.setString(2, middlenameField.getText());
            pstmt.setString(3, lastnameField.getText());
            pstmt.setString(4, courseField.getText());
            pstmt.setString(5, yearlevelComboBox.getSelectedItem().toString());
            pstmt.setInt(6, userId);

            int rowsAffected = pstmt.executeUpdate();

            ImageIcon icon = (ImageIcon) lblProfilePic.getIcon();
            if (icon != null) {
                byte[] imageData = imageToByteArray(icon.getImage());
                sql = "UPDATE users SET profile_picture = ? WHERE id = ?";
                pstmt = connection.prepareStatement(sql);
                pstmt.setBytes(1, imageData);
                pstmt.setInt(2, userId);
                pstmt.executeUpdate();
            }

            if (rowsAffected > 0) {
                sql = "SELECT firstName, middleName, lastName, email, course, yearLevel FROM users WHERE id = ?";
                pstmt = connection.prepareStatement(sql);
                pstmt.setInt(1, userId);
                ResultSet rs = pstmt.executeQuery();
                
                if (rs.next()) {
                    String name = rs.getString("firstName");
                    String email = rs.getString("email");
                    String course = rs.getString("course");
                    String yearLevel = rs.getString("yearLevel");

                    studentLandingPage.setUserData(name, email, course, yearLevel);
                }
                
//                JOptionPane.showMessageDialog(this, "Profile updated successfully!");
                studentLandingPage.loadProfilePicture();
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No user found with ID: " + userId);
            }

        } catch (SQLException e) {
            System.out.println("SQL Error: " + e.getMessage());
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e) {
            System.out.println("Number Format Error: " + e.getMessage());
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Invalid user ID format", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_savechangesButtonActionPerformed

