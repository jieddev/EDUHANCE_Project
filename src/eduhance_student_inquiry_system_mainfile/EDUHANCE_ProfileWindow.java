    private void loadUserData() {
        try {
            String userIdString = UserSession.getInstance().getUserId();
            String idNumber = userIdString.split(",")[0].split(":")[1];
            int userId = Integer.parseInt(idNumber);

            String sql = "SELECT firstName, middleName, lastName, course, yearLevel FROM users WHERE id = ?";
            PreparedStatement pstmt = connection.prepareStatement(sql);
            pstmt.setInt(1, userId);

            ResultSet rs = pstmt.executeQuery();

            if (rs.next()) {
                firstnameField.setText(rs.getString("firstName"));
                middlenameField.setText(rs.getString("middleName"));
                lastnameField.setText(rs.getString("lastName"));
                courseField.setText(rs.getString("course"));

            }

        } catch (SQLException e) {
            System.out.println("Error loading user data: " + e.getMessage());
            JOptionPane.showMessageDialog(this, 
                "Error loading user data", 
                "Database Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private byte[] imageToByteArray(Image image) {
        try {
            BufferedImage bi = new BufferedImage(
                image.getWidth(null), 
                image.getHeight(null), 
                BufferedImage.TYPE_INT_RGB);
            Graphics2D g2 = bi.createGraphics();
            g2.drawImage(image, 0, 0, null);
            g2.dispose();

            ByteArrayOutputStream baos = new ByteArrayOutputStream();
            ImageIO.write(bi, "jpg", baos);
            return baos.toByteArray();
        } catch (IOException e) {
            return null;
        }
    }
    
