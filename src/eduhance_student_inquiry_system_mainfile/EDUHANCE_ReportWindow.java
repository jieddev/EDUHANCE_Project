    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        StringBuilder reportDescription = new StringBuilder();

        if (jCheckBox1.isSelected()) {
            reportDescription.append("Bugs and Glitches, ");
        }
        if (jCheckBox2.isSelected()) {
            reportDescription.append("Data Staleness, ");
        }
        if (jCheckBox3.isSelected()) {
            reportDescription.append("Displaying Errors, ");
        }
        if (jCheckBox4.isSelected()) {
            reportDescription.append("Data Inconsistency, ");
        }
        if (jCheckBox5.isSelected()) {
            reportDescription.append("System Inefficiency, ");
        }

        String additionalComments = reportBox.getText().trim();
        if (!additionalComments.isEmpty()) {
            reportDescription.append("Additional Comments: ").append(additionalComments);
        }

        // Remove trailing comma if exists
        String finalDescription = reportDescription.toString().replaceAll(", $", "");

        if (finalDescription.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select atleast 1 issue to report.");
            return;
        }

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/eduhance_db", "root", "");

            String sql = "INSERT INTO reports (userId, reportDescription, dateSubmitted) VALUES (?, ?, NOW())";
            PreparedStatement pstmt = conn.prepareStatement(sql);

            String currentUserId = UserSession.getInstance().getUserId().replace(":", "").replaceAll("[^0-9]", "").trim(); ;
            pstmt.setString(1, currentUserId);
            pstmt.setString(2, finalDescription);

            pstmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Report submitted successfully!");

            // Clear all selections
            jCheckBox1.setSelected(false);
            jCheckBox2.setSelected(false);
            jCheckBox3.setSelected(false);
            jCheckBox4.setSelected(false);
            jCheckBox5.setSelected(false);
            reportBox.setText("");

            pstmt.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error submitting report: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_submitButtonActionPerformed

