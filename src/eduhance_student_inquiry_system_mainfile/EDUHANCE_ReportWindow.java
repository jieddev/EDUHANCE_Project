    private void setUsernameGreet(){
        String currentUserId = UserSession.getInstance().getUserId().replace(":", "");
        System.out.println("Current User ID: " + currentUserId);

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/eduhance_db", "root", "");
            String sql = "SELECT firstName FROM users WHERE id = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, currentUserId);

            System.out.println("Executing query for ID: " + currentUserId);
            ResultSet rs = pstmt.executeQuery();

            if (rs.next()) {
                String firstName = rs.getString("firstName");
                System.out.println("Found firstName: " + firstName);
                nameLoggedinLabel.setText("Hello, " + firstName + "! What would you like to report?");
            }

            rs.close();
            pstmt.close();
            conn.close();
        } catch (Exception e) {
            System.out.println("Error getting username: " + e.getMessage());
            e.printStackTrace();
        }
    }

